#!/usr/bin/env node
/**
 * Generates `src/lib/intl/keys.gen.ts`: the `MessageKey` union of every catalog
 * key under `src/lib/intl/messages/en/`.
 *
 * Spike-stage codegen (Milestone 0). It reads each `en/*.json`, drops the
 * ARB-style `@key` metadata entries, and emits a sorted string-literal union so
 * `t()` / `<Trans>` reject unknown keys at typecheck time. M1 generalizes this
 * into the check pipeline (freshness check + missing/dead-key report).
 *
 * Run from the desktop app dir: `node scripts/gen-message-keys.js`.
 */

import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const messagesDir = join(here, '..', 'src', 'lib', 'intl', 'messages', 'en')
const outFile = join(here, '..', 'src', 'lib', 'intl', 'keys.gen.ts')

/**
 * Collects every non-`@` key across all `en/*.json` catalog files.
 * @returns {string[]}
 */
function collectKeys() {
  /** @type {Set<string>} */
  const keys = new Set()
  for (const file of readdirSync(messagesDir)) {
    if (!file.endsWith('.json')) continue
    const json = JSON.parse(readFileSync(join(messagesDir, file), 'utf8'))
    for (const key of Object.keys(json)) {
      if (key.startsWith('@')) continue
      keys.add(key)
    }
  }
  return [...keys].sort()
}

/**
 * Renders the `keys.gen.ts` source for the given sorted key list.
 * @param {string[]} keys
 * @returns {string}
 */
function emit(keys) {
  const union = keys.length > 0 ? keys.map((k) => `  | '${k}'`).join('\n') : '  never'
  return `// AUTO-GENERATED by scripts/gen-message-keys.js. Do not edit by hand.
// Run \`node scripts/gen-message-keys.js\` (or the check pipeline) to refresh.

/** Every key present in \`messages/en/*.json\`. A wrong key is a typecheck error. */
export type MessageKey =
${union}
`
}

const keys = collectKeys()
writeFileSync(outFile, emit(keys))
console.log(`Wrote ${String(keys.length)} message keys to keys.gen.ts`)
